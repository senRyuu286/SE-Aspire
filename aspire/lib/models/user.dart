class User{
  final int userID;
  final String email;
  final String fName;
  final String lName;
  final String password;
  final int age;
  final double progress;

  const User({
    required this.userID, 
    required this.email, 
    required this.fName, 
    required this.lName, 
    required this.password, 
    required this.age, 
    required this.progress,
    });
}